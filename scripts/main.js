const storageKey = "portfolio-lang-v3";
const textNodes = document.querySelectorAll("[data-i18n]");
const langButtons = document.querySelectorAll("[data-lang-trigger]");
const navLinks = document.querySelectorAll("[data-nav-target]");
const sections = document.querySelectorAll("[data-section]");

function setActiveTab(sectionId) {
  navLinks.forEach((link) => {
    link.classList.toggle("is-active", link.dataset.navTarget === sectionId);
  });
}

function setLanguage(lang) {
  const dictionary = translations[lang];
  if (!dictionary) return;

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  document.documentElement.lang = lang;

  langButtons.forEach((button) => {
    const active = button.dataset.langTrigger === lang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  localStorage.setItem(storageKey, lang);
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.langTrigger));
});

function syncActiveTabToScroll() {
  const marker = window.innerHeight * 0.28;
  let currentSectionId = sections[0]?.id;

  sections.forEach((section) => {
    const rect = section.getBoundingClientRect();
    if (rect.top <= marker) {
      currentSectionId = section.id;
    }
  });

  if (currentSectionId) {
    setActiveTab(currentSectionId);
  }
}

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    setActiveTab(link.dataset.navTarget);
  });
});

window.addEventListener("scroll", syncActiveTabToScroll, { passive: true });
window.addEventListener("load", syncActiveTabToScroll);

const defaultLanguage = navigator.language && navigator.language.toLowerCase().startsWith("fr") ? "fr" : "en";
setLanguage(localStorage.getItem(storageKey) || defaultLanguage);
syncActiveTabToScroll();


